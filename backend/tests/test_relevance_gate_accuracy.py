# ── 准确率测试 ──

@pytest.mark.slow
@pytest.mark.asyncio
async def test_accuracy_200_samples():
    """200 条全新样本准确率测试。"""
    gate = RelevanceGate()

    correct = 0
    false_positives = []   # 实际 ignore，预测 should_enter
    false_negatives = []   # 实际 should_enter，预测 ignore

    for text, expected in TEST_SAMPLES:
        utt = _utt(text)
        predicted = await gate.is_relevant(utt)
        if predicted == bool(expected):
            correct += 1
        elif expected == 0:
            false_positives.append(text)
        else:
            false_negatives.append(text)

    accuracy = correct / len(TEST_SAMPLES)
    print(f"\n{'=' * 60}")
    print(f"样本总数: {len(TEST_SAMPLES)}")
    print(f"正确: {correct}")
    print(f"准确率: {accuracy:.2%}")
    print(f"假阳性 (ignore → should_enter): {len(false_positives)} 条")
    for t in false_positives[:10]:
        print(f"  FP: {t}")
    print(f"假阴性 (should_enter → ignore): {len(false_negatives)} 条")
    for t in false_negatives[:10]:
        print(f"  FN: {t}")
    print(f"{'=' * 60}\n")

    # 阈值：准确率 >= 95% 视为通过
    assert accuracy >= 0.95, f"准确率 {accuracy:.2%} 低于 95%"


# ── 延迟测试 ──

@pytest.mark.slow
@pytest.mark.asyncio
async def test_latency_single():
    """单条推理延迟测试（毫秒级）。"""
    gate = RelevanceGate()
    utt = _utt("公司单方面降薪合法吗")

    # warm-up
    await gate.is_relevant(utt)

    times = []
    for _ in range(50):
        t0 = time.perf_counter()
        await gate.is_relevant(utt)
        t1 = time.perf_counter()
        times.append((t1 - t0) * 1000)

    avg = sum(times) / len(times)
    p50 = sorted(times)[len(times) // 2]
    p99 = sorted(times)[int(len(times) * 0.99)]

    print(f"\n{'=' * 60}")
    print(f"单条推理延迟 (50 次)")
    print(f"  平均: {avg:.2f} ms")
    print(f"  P50:  {p50:.2f} ms")
    print(f"  P99:  {p99:.2f} ms")
    print(f"{'=' * 60}\n")

    # 阈值：平均延迟 < 100ms
    assert avg < 100, f"平均延迟 {avg:.2f} ms 超过 100ms"


@pytest.mark.slow
@pytest.mark.asyncio
async def test_latency_batch():
    """并发 10 条推理延迟测试。"""
    gate = RelevanceGate()
    texts = [t for t, _ in TEST_SAMPLES[:10]]
    utts = [_utt(t) for t in texts]

    # warm-up
    await asyncio.gather(*[gate.is_relevant(u) for u in utts])

    t0 = time.perf_counter()
    await asyncio.gather(*[gate.is_relevant(u) for u in utts])
    t1 = time.perf_counter()

    total_ms = (t1 - t0) * 1000
    per_item = total_ms / len(utts)

    print(f"\n{'=' * 60}")
    print(f"并发 10 条推理")
    print(f"  总耗时: {total_ms:.2f} ms")
    print(f"  平均每条: {per_item:.2f} ms")
    print(f"{'=' * 60}\n")

    # 阈值：并发总耗时 < 500ms
    assert total_ms < 500, f"并发总耗时 {total_ms:.2f} ms 超过 500ms"
